    private void btn_correctMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_correctMouseClicked
        // TODO add your handling code here:
        String[] words=txt_content.getText().split(" ");
        for(int ctr=0; ctr<words.length; ctr++){
            words[ctr]=spellchecker.correct(words[ctr]);
        }
        String newContent="";
        for(String word : words){
            newContent=newContent.concat(word+" ");
        }
        txt_content.setText(newContent);
    }//GEN-LAST:event_btn_correctMouseClicked

    private void btn_cancelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cancelMouseClicked
        // TODO add your handling code here:
        if(fromCamera){
            new CameraForm(this.tts).setVisible(true);
            dispose();
        }
        else{
            new ReadForm(this.tts,this.fileName).setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btn_cancelMouseClicked

    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked
        // TODO add your handling code here:
        try{
            File file=new File(Global.FILES_DIR+"/"+txt_filename.getText());
            file.createNewFile();
            FileWriter wr=new FileWriter(file);
            wr.write(txt_content.getText());
            wr.close();
            new ReadForm(this.tts,txt_filename.getText()).setVisible(true);
            dispose();
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btn_saveMouseClicked

