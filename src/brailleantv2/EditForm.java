    private void init(){
        
        this.getContentPane().setBackground(Color.cyan);
        try{
            spellchecker=new Spelling(Global.DICT_DIR);
            java.io.File file=new java.io.File("/home/pi/Documents/Brailleant/out.txt");
            System.out.println("Filename: "+file.getName());
            Date now=new Date();
            this.txt_filename.setText(now.toString());
            java.util.Scanner sc = new java.util.Scanner(file);
            while (sc.hasNext()) {
                originalString = originalString.concat(sc.nextLine() + "\n");
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
        this.txt_content.setText(originalString);
        spellchecker.register(txt_content);
    }
